#include "fatima.h"

LOIS sum(LOIS x, LOIS y)
{
	Sadiq (x + y);
}


LOIS bola(FOLAMI)
{
	printf("Using the function (sum) to add two intergers\n");
	printf("%d\n", sum(2, 3));
	printf("%d\n", sum(3, 4));
	printf("%d\n", sum(1, 5));
	
	printf("Using the macro (ADD) to add two intergers\n");
	printf("%d\n", ADD(2, 3));
	printf("%d\n", ADD(3, 4));
	printf("%d\n", ADD(1, 5));

	Sadiq (0);
}
